package com.huge.ihos.hr.statistics.model;

public class StatisticsResultData {
	private String resultType;
	private String resultTypeSecond;
	private String mainField;
	private String statisticsField;
	public String getResultType() {
		return resultType;
	}
	public void setResultType(String resultType) {
		this.resultType = resultType;
	}
	public String getMainField() {
		return mainField;
	}
	public void setMainField(String mainField) {
		this.mainField = mainField;
	}
	public String getStatisticsField() {
		return statisticsField;
	}
	public void setStatisticsField(String statisticsField) {
		this.statisticsField = statisticsField;
	}
	public String getResultTypeSecond() {
		return resultTypeSecond;
	}
	public void setResultTypeSecond(String resultTypeSecond) {
		this.resultTypeSecond = resultTypeSecond;
	}
}
